package com.sathya.assignment1;

public class Student {
	private int studentRoll;
	private String studentName;
	private Marks studentMarks;
	private School studentSchool;
	public int getStudentRoll() {
		return studentRoll;
	}
	public void setStudentRoll(int studentRoll) {
		this.studentRoll = studentRoll;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public Marks getStudentMarks() {
		return studentMarks;
	}
	public void setStudentMarks(Marks studentMarks) {
		this.studentMarks = studentMarks;
	}
	public School getStudentSchool() {
		return studentSchool;
	}
	public void setStudentSchool(School studentSchool) {
		this.studentSchool = studentSchool;
	}
	

}
